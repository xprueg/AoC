use strict;
use warnings;
use List::Util qw/ sum /;
use List::MoreUtils qw/ pairwise /;
use feature qw/ say state /;

use constant PART => $ARGV[0] // 1;

goto "PART_${\ PART}";

PART_1: {
    my (@keys, @locks);
    for (;;) {
        last if eof DATA;

        chomp(my $head = <DATA>);
        chomp(my $r1 = <DATA>);
        chomp(my $r2 = <DATA>);
        chomp(my $r3 = <DATA>);
        chomp(my $r4 = <DATA>);
        chomp(my $r5 = <DATA>);
        chomp(my $tail = <DATA>);
        my $blank = <DATA>;

        my $is_lock = $head =~ /^#/;
        my @schema = (0) x length $head;

        for (my $i = 0; $i < length $r1; ++$i) {
            ++$schema[$i] if substr($r1, $i, 1) eq '#';
            ++$schema[$i] if substr($r2, $i, 1) eq '#';
            ++$schema[$i] if substr($r3, $i, 1) eq '#';
            ++$schema[$i] if substr($r4, $i, 1) eq '#';
            ++$schema[$i] if substr($r5, $i, 1) eq '#';
        }

        push @{ $is_lock ? \@locks : \@keys } => \@schema;
    } 

    say sum map {
        my $key = $_;
        map {
            my $lock = $_;

               $key->[0] + $lock->[0] <= 5
            && $key->[1] + $lock->[1] <= 5
            && $key->[2] + $lock->[2] <= 5
            && $key->[3] + $lock->[3] <= 5
            && $key->[4] + $lock->[4] <= 5;
        } @locks;
    } @keys;

    exit 0;
}

